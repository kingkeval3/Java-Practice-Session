package com.java.demo.pojos;

public class MergedCellPojo {

    private Integer firstRow;
    private Integer lastRow;
    private Integer firstColumn;
    private Integer lastColumn;
    private Integer id;

    /**
     * @return the firstRow
     */
    public Integer getFirstRow() {
        return firstRow;
    }

    /**
     * @param firstRow the firstRow to set
     */
    public void setFirstRow(Integer firstRow) {
        this.firstRow = firstRow;
    }

    /**
     * @return the lastRow
     */
    public Integer getLastRow() {
        return lastRow;
    }

    /**
     * @param lastRow the lastRow to set
     */
    public void setLastRow(Integer lastRow) {
        this.lastRow = lastRow;
    }

    /**
     * @return the firstColumn
     */
    public Integer getFirstColumn() {
        return firstColumn;
    }

    /**
     * @param firstColumn the firstColumn to set
     */
    public void setFirstColumn(Integer firstColumn) {
        this.firstColumn = firstColumn;
    }

    /**
     * @return the lastColumn
     */
    public Integer getLastColumn() {
        return lastColumn;
    }

    /**
     * @param lastColumn the lastColumn to set
     */
    public void setLastColumn(Integer lastColumn) {
        this.lastColumn = lastColumn;
    }

    /**
     * @return the id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Integer id) {
        this.id = id;
    }

}